'''program to illustrate various
datatypes in python'''
print("\nNumeric datatype:")
a=5
print("type of (",a,")is:",type(a))
a=5.0
print("type of (",a,")is:",type(a))
a=2+4j
print("type of (",a,")is:",type(a))

print("\nString datatype:")
a="rakesh"
print("type of (",a,")is:",type(a))

print("\nBoolean datatype:")
a=True
print("type of (",a,")is:",type(a))

print("\nList datatype:")
a=[1,2,"r","a"]
print("type of (",a,")is:",type(a))

print("\nSet datatype:")
a={1,2,3,"s","r"}
print("type of (",a,")is:",type(a))

print("\nTuple datatype:")
a=(1,2,3,"r")
print("type of (",a,")is:",type(a))

print("\nDictionary datatype:")
a={1:"mangalore",2:"bangalore"}
print("type of (",a,")is:",type(a))

print("\nBytes datatype:")
a=b'123'
print("type of (",a,")is:",type(a))